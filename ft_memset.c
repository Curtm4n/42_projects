#include <string.h>

void	*ft_memset(void *s, int c, size_t n)
{
	int i;

	i = 0;
	while (n > 0)
	{
		((unsigned char *)s)[i] = c;
		n--;
		i++;
	}
	((unsigned char *)s)[i] = '\0';
	return (s);
}
